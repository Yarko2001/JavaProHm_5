public class Robot extends Participant {

  public Robot(String name, double topJumpHeight, double topRunDistance) {
    super(name, topJumpHeight, topRunDistance);
  }

  @Override
  public String jump() {
    return super.jump();
  }

  @Override
  public String run() {
    return super.run();
  }

  @Override
  public String getName() {
    return super.getName();
  }
}
